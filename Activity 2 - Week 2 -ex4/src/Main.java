class loopFor{
    public static void main(String[] args) {

        //ordem crescente:
        for(int a = 1; a <= 10; a++){
            System.out.print(a+" ");
        }

        System.out.println();

        //ordem decrescente:
        for(int b = 10; b >= 1; b--){
            System.out.print(b+" ");
        }

    }
}